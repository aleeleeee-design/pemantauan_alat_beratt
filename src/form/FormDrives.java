    private void tampilData() {

    model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Officer");
    model.addColumn("Vehicle");
    model.addColumn("Latitude");
    model.addColumn("Longitude");
    model.addColumn("Date");
    model.addColumn("Active");

    tblDrives.setModel(model);

    try {

        Connection c = Koneksi.getKoneksi();

        String sql =
        "SELECT drives.id, officers.name, vehicles.model, "
        + "drives.latitude, drives.longitude, "
        + "drives.date, drives.active "
        + "FROM drives "
        + "JOIN officers ON drives.officer_id = officers.id "
        + "JOIN vehicles ON drives.vehicle_id = vehicles.id";

        PreparedStatement p = c.prepareStatement(sql);

        ResultSet r = p.executeQuery();

        while (r.next()) {

            Object[] data = {

                r.getString("id"),
                r.getString("name"),
                r.getString("model"),
                r.getString("latitude"),
                r.getString("longitude"),
                r.getString("date"),
                r.getString("active")

            };

            model.addRow(data);

        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e);

    }

}
