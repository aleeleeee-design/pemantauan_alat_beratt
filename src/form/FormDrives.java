    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {

        Drive drive = new Drive();

        Connection c = Koneksi.getKoneksi();

        // AMBIL ID OFFICER
        String sqlOfficer =
        "SELECT id FROM officers WHERE name=?";

        PreparedStatement pOfficer =
        c.prepareStatement(sqlOfficer);

        pOfficer.setString(1,
        cbOfficer.getSelectedItem().toString());

        ResultSet rOfficer =
        pOfficer.executeQuery();

        String officerId = "";

        if (rOfficer.next()) {

            officerId = rOfficer.getString("id");

        }

        // AMBIL ID VEHICLE
        String sqlVehicle =
        "SELECT id FROM vehicles WHERE model=?";

        PreparedStatement pVehicle =
        c.prepareStatement(sqlVehicle);

        pVehicle.setString(1,
        cbVehicle.getSelectedItem().toString());

        ResultSet rVehicle =
        pVehicle.executeQuery();

        String vehicleId = "";

        if (rVehicle.next()) {

            vehicleId = rVehicle.getString("id");

        }

        // SET OBJECT OOP
        drive.setOfficerId(officerId);
        drive.setVehicleId(vehicleId);
        drive.setLatitude(txtLatitude.getText());
        drive.setLongitude(txtLongitude.getText());
        drive.setDate(txtDate.getText());
        drive.setActive(chkActive.isSelected());

        // INSERT
        String sql =
        "INSERT INTO drives "
        + "(officer_id, vehicle_id, active, latitude, longitude, date) "
        + "VALUES (?, ?, ?, ?, ?, ?)";

        PreparedStatement p =
        c.prepareStatement(sql);

        p.setString(1, drive.getOfficerId());
        p.setString(2, drive.getVehicleId());
        p.setBoolean(3, drive.isActive());
        p.setString(4, drive.getLatitude());
        p.setString(5, drive.getLongitude());
        p.setString(6, drive.getDate());

        p.executeUpdate();

        JOptionPane.showMessageDialog(null,
        "Data monitoring berhasil disimpan");

        tampilData();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e);

    }
    }//GEN-LAST:event_btnSimpanActionPerformed

